"""Runtime emitter helpers for runtime emit simd."""

from __future__ import annotations

__all__ = [
    "emit_runtime_simd",
    "emit_simd_header",
    "emit_simd_basic_ops",
    "emit_simd_advanced_ops",
]


def emit_runtime_simd(self) -> None:
    """Emit SIMD-related runtime helpers."""
    if "simd" not in self._needs.helpers:
        return
    self._emit_simd_header()
    self._emit_simd_basic_ops()
    self._emit_simd_advanced_ops()


def emit_simd_header(self) -> None:
    """Emit SIMD header and type definitions."""
    self._output.append("/* SIMD intrinsics - multi-width support */")
    self._output.append("#ifdef __AVX2__")
    self._output.append("#include <immintrin.h>  /* AVX2 */")
    self._output.append("typedef __m128i vec128;")
    self._output.append("typedef __m256i vec256;")
    self._output.append("#define HAVE_AVX2 1")
    self._output.append("#elif defined(__SSE4_1__)")
    self._output.append("#include <smmintrin.h>  /* SSE4.1 */")
    self._output.append("typedef __m128i vec128;")
    self._output.append("typedef struct { __m128i lo; __m128i hi; } vec256;")
    self._output.append("#elif defined(__SSE2__)")
    self._output.append("#include <emmintrin.h>  /* SSE2 */")
    self._output.append("typedef __m128i vec128;")
    self._output.append("typedef struct { __m128i lo; __m128i hi; } vec256;")
    self._output.append("#else")
    self._output.append("typedef struct { int64_t v[2]; } vec128;")
    self._output.append("typedef struct { int64_t v[4]; } vec256;")
    self._output.append("#endif")
    self._output.append("")


def emit_simd_basic_ops(self) -> None:
    """Emit basic SIMD operations with multi-width support."""
    self._output.append("/* Helper to get vector width from type string */")
    self._output.append("static int get_vec_width(const char *type) {")
    self._output.append("    if (!type) return 16;")
    self._output.append('    if (strstr(type, "64") || strstr(type, "512")) return 64;')
    self._output.append('    if (strstr(type, "32") || strstr(type, "256")) return 32;')
    self._output.append("    return 16;")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec128 vec_broadcast(int64_t val, const char *type) {")
    self._output.append("    (void)type;")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    return _mm_set1_epi8((char)val);")
    self._output.append("#else")
    self._output.append("    vec128 v; v.v[0] = val; v.v[1] = val; return v;")
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append(
        "static vec256 vec_broadcast256(int64_t val, const char *type) {"
    )
    self._output.append("    (void)type;")
    self._output.append("    vec256 v;")
    self._output.append("#ifdef __AVX2__")
    self._output.append("    v = _mm256_set1_epi8((char)val);")
    self._output.append("#else")
    self._output.append("    v.lo = _mm_set1_epi8((char)val);")
    self._output.append("    v.hi = _mm_set1_epi8((char)val);")
    self._output.append("#endif")
    self._output.append("    return v;")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec128 vec_load(const void *ptr, const char *type) {")
    self._output.append("    (void)type;")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    return _mm_loadu_si128((const __m128i*)ptr);")
    self._output.append("#else")
    self._output.append("    vec128 v; memcpy(&v, ptr, 16); return v;")
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append(
        "static vec256 vec_load256(const void *ptr, const char *type) {"
    )
    self._output.append("    (void)type;")
    self._output.append("    vec256 v;")
    self._output.append("#ifdef __AVX2__")
    self._output.append("    v = _mm256_loadu_si256((const __m256i*)ptr);")
    self._output.append("#else")
    self._output.append("    v.lo = _mm_loadu_si128((const __m128i*)ptr);")
    self._output.append(
        "    v.hi = _mm_loadu_si128((const __m128i*)((const char*)ptr + 16));"
    )
    self._output.append("#endif")
    self._output.append("    return v;")
    self._output.append("}")
    self._output.append("")
    # vec_loadu256 -- explicit-unaligned alias of vec_load256.
    # Both already use unaligned intrinsics; the alias is for symmetry
    # with the LLVM backend's separation of aligned vs unaligned loads.
    self._output.append(
        "static vec256 vec_loadu256(const void *ptr, const char *type) {"
    )
    self._output.append("    return vec_load256(ptr, type);")
    self._output.append("}")
    self._output.append("")
    # vec_store256 / vec_storeu256 -- unaligned 256-bit stores. The C
    # backend was previously missing vec_store entirely; both are added
    # here. For 128-bit and 512-bit equivalents, see follow-up ticket.
    self._output.append("static int64_t vec_store256(void *ptr, vec256 v) {")
    self._output.append("#ifdef __AVX2__")
    self._output.append("    _mm256_storeu_si256((__m256i*)ptr, v);")
    self._output.append("#else")
    self._output.append("    _mm_storeu_si128((__m128i*)ptr, v.lo);")
    self._output.append("    _mm_storeu_si128((__m128i*)((char*)ptr + 16), v.hi);")
    self._output.append("#endif")
    self._output.append("    return 0;")
    self._output.append("}")
    self._output.append("")
    self._output.append("static int64_t vec_storeu256(void *ptr, vec256 v) {")
    self._output.append("    return vec_store256(ptr, v);")
    self._output.append("}")
    self._output.append("")
    # vec_shr256 / vec_shl256 -- per-lane variable shift on i64 elements.
    # AVX2 provides _mm256_srlv_epi64 / _mm256_sllv_epi64 directly. The
    # SSE-only fallback unpacks to scalar lanes and re-packs.
    self._output.append("static vec256 vec_shr256(vec256 a, vec256 b) {")
    self._output.append("    vec256 v;")
    self._output.append("#ifdef __AVX2__")
    self._output.append("    v = _mm256_srlv_epi64(a, b);")
    self._output.append("#else")
    self._output.append(
        "    /* Scalar fallback: 4 x i64 lanes, treated as uint64 for logical shift */"
    )
    self._output.append("    union { __m128i v; uint64_t u[2]; } al, bl, ah, bh;")
    self._output.append("    al.v = a.lo; bl.v = b.lo; ah.v = a.hi; bh.v = b.hi;")
    self._output.append("    al.u[0] >>= bl.u[0]; al.u[1] >>= bl.u[1];")
    self._output.append("    ah.u[0] >>= bh.u[0]; ah.u[1] >>= bh.u[1];")
    self._output.append("    v.lo = al.v; v.hi = ah.v;")
    self._output.append("#endif")
    self._output.append("    return v;")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec256 vec_shl256(vec256 a, vec256 b) {")
    self._output.append("    vec256 v;")
    self._output.append("#ifdef __AVX2__")
    self._output.append("    v = _mm256_sllv_epi64(a, b);")
    self._output.append("#else")
    self._output.append("    union { __m128i v; uint64_t u[2]; } al, bl, ah, bh;")
    self._output.append("    al.v = a.lo; bl.v = b.lo; ah.v = a.hi; bh.v = b.hi;")
    self._output.append("    al.u[0] <<= bl.u[0]; al.u[1] <<= bl.u[1];")
    self._output.append("    ah.u[0] <<= bh.u[0]; ah.u[1] <<= bh.u[1];")
    self._output.append("    v.lo = al.v; v.hi = ah.v;")
    self._output.append("#endif")
    self._output.append("    return v;")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec128 vec_cmpeq(vec128 a, vec128 b) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    return _mm_cmpeq_epi8(a, b);")
    self._output.append("#else")
    self._output.append("    (void)a; (void)b; vec128 v = {0}; return v;")
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec256 vec_cmpeq256(vec256 a, vec256 b) {")
    self._output.append("    vec256 v;")
    self._output.append("#ifdef __AVX2__")
    self._output.append("    v = _mm256_cmpeq_epi8(a, b);")
    self._output.append("#else")
    self._output.append("    v.lo = _mm_cmpeq_epi8(a.lo, b.lo);")
    self._output.append("    v.hi = _mm_cmpeq_epi8(a.hi, b.hi);")
    self._output.append("#endif")
    self._output.append("    return v;")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec256 vec_add256(vec256 a, vec256 b) {")
    self._output.append("    vec256 v;")
    self._output.append("#ifdef __AVX2__")
    self._output.append("    v = _mm256_add_epi8(a, b);")
    self._output.append("#else")
    self._output.append("    v.lo = _mm_add_epi8(a.lo, b.lo);")
    self._output.append("    v.hi = _mm_add_epi8(a.hi, b.hi);")
    self._output.append("#endif")
    self._output.append("    return v;")
    self._output.append("}")
    self._output.append("")
    self._output.append("static int64_t vec_extract256(vec256 v, int64_t idx) {")
    self._output.append("#ifdef __AVX2__")
    self._output.append("    union { __m256i v; int8_t b[32]; } u; u.v = v;")
    self._output.append("    return (int64_t)u.b[idx & 31];")
    self._output.append("#else")
    self._output.append("    union { __m128i v; int8_t b[16]; } lo, hi;")
    self._output.append("    lo.v = v.lo; hi.v = v.hi;")
    self._output.append("    int i = idx & 31;")
    self._output.append("    return i < 16 ? (int64_t)lo.b[i] : (int64_t)hi.b[i - 16];")
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static int64_t vec_movemask(vec128 v) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    return (int64_t)_mm_movemask_epi8(v);")
    self._output.append("#else")
    self._output.append("    (void)v; return 0;")
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static int64_t vec_movemask256(vec256 v) {")
    self._output.append("#ifdef __AVX2__")
    self._output.append("    return (int64_t)_mm256_movemask_epi8(v);")
    self._output.append("#else")
    movemask_code = "    return (int64_t)(((uint32_t)_mm_movemask_epi8(v.hi) << 16)"
    movemask_code += " | (uint32_t)_mm_movemask_epi8(v.lo));"
    self._output.append(movemask_code)
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")

    # 512-bit variants (emulated using 4x 128-bit vectors)
    self._output.append("/* 512-bit vector type (emulated) */")
    self._output.append("typedef struct { vec128 v0, v1, v2, v3; } vec512;")
    self._output.append("")
    self._output.append(
        "static vec512 vec_broadcast512(int64_t val, const char *type) {"
    )
    self._output.append("    (void)type;")
    self._output.append("    vec512 v;")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    __m128i b = _mm_set1_epi8((char)val);")
    self._output.append("    v.v0 = b; v.v1 = b; v.v2 = b; v.v3 = b;")
    self._output.append("#else")
    self._output.append("    v.v0.v[0] = val; v.v0.v[1] = val;")
    self._output.append("    v.v1.v[0] = val; v.v1.v[1] = val;")
    self._output.append("    v.v2.v[0] = val; v.v2.v[1] = val;")
    self._output.append("    v.v3.v[0] = val; v.v3.v[1] = val;")
    self._output.append("#endif")
    self._output.append("    return v;")
    self._output.append("}")
    self._output.append("")
    self._output.append(
        "static vec512 vec_load512(const void *ptr, const char *type) {"
    )
    self._output.append("    (void)type;")
    self._output.append("    vec512 v;")
    self._output.append("    const __m128i *p = (const __m128i*)ptr;")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    v.v0 = _mm_loadu_si128(p);")
    self._output.append("    v.v1 = _mm_loadu_si128(p + 1);")
    self._output.append("    v.v2 = _mm_loadu_si128(p + 2);")
    self._output.append("    v.v3 = _mm_loadu_si128(p + 3);")
    self._output.append("#else")
    self._output.append("    const int64_t *ip = (const int64_t*)ptr;")
    self._output.append("    v.v0.v[0] = ip[0]; v.v0.v[1] = ip[1];")
    self._output.append("    v.v1.v[0] = ip[2]; v.v1.v[1] = ip[3];")
    self._output.append("    v.v2.v[0] = ip[4]; v.v2.v[1] = ip[5];")
    self._output.append("    v.v3.v[0] = ip[6]; v.v3.v[1] = ip[7];")
    self._output.append("#endif")
    self._output.append("    return v;")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec512 vec_cmpeq512(vec512 a, vec512 b) {")
    self._output.append("    vec512 v;")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    v.v0 = _mm_cmpeq_epi8(a.v0, b.v0);")
    self._output.append("    v.v1 = _mm_cmpeq_epi8(a.v1, b.v1);")
    self._output.append("    v.v2 = _mm_cmpeq_epi8(a.v2, b.v2);")
    self._output.append("    v.v3 = _mm_cmpeq_epi8(a.v3, b.v3);")
    self._output.append("#else")
    self._output.append("    (void)a; (void)b;")
    self._output.append("    v.v0.v[0] = 0; v.v0.v[1] = 0;")
    self._output.append("    v.v1.v[0] = 0; v.v1.v[1] = 0;")
    self._output.append("    v.v2.v[0] = 0; v.v2.v[1] = 0;")
    self._output.append("    v.v3.v[0] = 0; v.v3.v[1] = 0;")
    self._output.append("#endif")
    self._output.append("    return v;")
    self._output.append("}")
    self._output.append("")
    self._output.append("static int64_t vec_movemask512(vec512 v) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    int64_t m0 = _mm_movemask_epi8(v.v0);")
    self._output.append("    int64_t m1 = _mm_movemask_epi8(v.v1);")
    self._output.append("    int64_t m2 = _mm_movemask_epi8(v.v2);")
    self._output.append("    int64_t m3 = _mm_movemask_epi8(v.v3);")
    self._output.append("    return m0 | (m1 << 16) | (m2 << 32) | (m3 << 48);")
    self._output.append("#else")
    self._output.append("    (void)v; return 0;")
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static int64_t vec_extract512(vec512 v, int64_t idx) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    union { __m128i v; int8_t b[16]; } u;")
    self._output.append("    int i = idx & 63;")
    self._output.append("    if (i < 16) { u.v = v.v0; return (int64_t)u.b[i]; }")
    self._output.append("    if (i < 32) { u.v = v.v1; return (int64_t)u.b[i - 16]; }")
    self._output.append("    if (i < 48) { u.v = v.v2; return (int64_t)u.b[i - 32]; }")
    self._output.append("    u.v = v.v3; return (int64_t)u.b[i - 48];")
    self._output.append("#else")
    self._output.append("    (void)v; (void)idx; return 0;")
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec512 vec_mul512(vec512 a, vec512 b) {")
    self._output.append("    vec512 v;")
    self._output.append("#ifdef __SSE4_1__")
    self._output.append("    v.v0 = _mm_mullo_epi32(a.v0, b.v0);")
    self._output.append("    v.v1 = _mm_mullo_epi32(a.v1, b.v1);")
    self._output.append("    v.v2 = _mm_mullo_epi32(a.v2, b.v2);")
    self._output.append("    v.v3 = _mm_mullo_epi32(a.v3, b.v3);")
    self._output.append("#else")
    self._output.append("    (void)a; (void)b;")
    self._output.append("#endif")
    self._output.append("    return v;")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec128 vec_add(vec128 a, vec128 b) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    return _mm_add_epi64(a, b);")
    self._output.append("#else")
    self._output.append(
        "    vec128 v; v.v[0] = a.v[0] + b.v[0]; v.v[1] = a.v[1] + b.v[1]; return v;"
    )
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec128 vec_sub(vec128 a, vec128 b) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    return _mm_sub_epi64(a, b);")
    self._output.append("#else")
    self._output.append(
        "    vec128 v; v.v[0] = a.v[0] - b.v[0]; v.v[1] = a.v[1] - b.v[1]; return v;"
    )
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")


def emit_simd_advanced_ops(self) -> None:
    """Emit advanced SIMD operations."""
    self._output.append("static vec128 vec_mul(vec128 a, vec128 b) {")
    self._output.append("#ifdef __SSE4_1__")
    self._output.append("    return _mm_mullo_epi32(a, b);")
    self._output.append("#elif defined(__SSE2__)")
    self._output.append("    /* SSE2 fallback - element-wise multiply */")
    self._output.append(
        "    __m128i a13 = _mm_shuffle_epi32(a, 0xF5);  /* a1, a3, a1, a3 */"
    )
    self._output.append(
        "    __m128i b13 = _mm_shuffle_epi32(b, 0xF5);  /* b1, b3, b1, b3 */"
    )
    self._output.append("    __m128i prod02 = _mm_mul_epu32(a, b);")
    self._output.append("    __m128i prod13 = _mm_mul_epu32(a13, b13);")
    self._output.append("    __m128i prod01 = _mm_unpacklo_epi32(prod02, prod13);")
    self._output.append("    __m128i prod23 = _mm_unpackhi_epi32(prod02, prod13);")
    self._output.append("    return _mm_unpacklo_epi64(prod01, prod23);")
    self._output.append("#else")
    self._output.append(
        "    vec128 v; v.v[0] = a.v[0] * b.v[0]; v.v[1] = a.v[1] * b.v[1]; return v;"
    )
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec128 vec_or(vec128 a, vec128 b) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    return _mm_or_si128(a, b);")
    self._output.append("#else")
    self._output.append(
        "    vec128 v; v.v[0] = a.v[0] | b.v[0]; v.v[1] = a.v[1] | b.v[1]; return v;"
    )
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec128 vec_and(vec128 a, vec128 b) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    return _mm_and_si128(a, b);")
    self._output.append("#else")
    self._output.append(
        "    vec128 v; v.v[0] = a.v[0] & b.v[0]; v.v[1] = a.v[1] & b.v[1]; return v;"
    )
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec128 vec_xor(vec128 a, vec128 b) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append("    return _mm_xor_si128(a, b);")
    self._output.append("#else")
    self._output.append(
        "    vec128 v; v.v[0] = a.v[0] ^ b.v[0]; v.v[1] = a.v[1] ^ b.v[1]; return v;"
    )
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec128 vec_nand(vec128 a, vec128 b) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append(
        "    return _mm_andnot_si128(_mm_and_si128(a, b), _mm_set1_epi64x(-1LL));"
    )
    self._output.append("#else")
    self._output.append(
        "    vec128 v; v.v[0] = ~(a.v[0] & b.v[0]); v.v[1] = ~(a.v[1] & b.v[1]); return v;"
    )
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec128 vec_nor(vec128 a, vec128 b) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append(
        "    return _mm_andnot_si128(_mm_or_si128(a, b), _mm_set1_epi64x(-1LL));"
    )
    self._output.append("#else")
    self._output.append(
        "    vec128 v; v.v[0] = ~(a.v[0] | b.v[0]); v.v[1] = ~(a.v[1] | b.v[1]); return v;"
    )
    self._output.append("#endif")
    self._output.append("}")
    self._output.append("")
    self._output.append("static vec128 vec_xnor(vec128 a, vec128 b) {")
    self._output.append("#ifdef __SSE2__")
    self._output.append(
        "    return _mm_andnot_si128(_mm_xor_si128(a, b), _mm_set1_epi64x(-1LL));"
    )
    self._output.append("#else")
    self._output.append(
        "    vec128 v; v.v[0] = ~(a.v[0] ^ b.v[0]); v.v[1] = ~(a.v[1] ^ b.v[1]); return v;"
    )
    self._output.append("#endif")
