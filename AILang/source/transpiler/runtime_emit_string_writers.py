"""Typed string/print writer helper emission for C runtime."""

from __future__ import annotations


def emit_typed_writer_helpers(self) -> None:
    """Emit no-allocation typed stdout writers (P11 print fast path)."""
    self._output.append("#ifndef AILANG_FREESTANDING")
    self._output.append("static void ailang_write_u64(FILE *out, uint64_t n) {")
    self._output.append("    char tmp[24];")
    self._output.append("    int i = 0;")
    self._output.append("    do {")
    self._output.append("        tmp[i++] = (char)('0' + (n % 10U));")
    self._output.append("        n /= 10U;")
    self._output.append("    } while (n != 0U);")
    self._output.append("    while (i > 0) fputc(tmp[--i], out);")
    self._output.append("}")
    self._output.append("")

    self._output.append("static void ailang_write_i64(FILE *out, int64_t n) {")
    self._output.append("    uint64_t u;")
    self._output.append("    if (n < 0) {")
    self._output.append("        fputc('-', out);")
    self._output.append("        u = (uint64_t)(-(n + 1)) + 1U;")
    self._output.append("    } else {")
    self._output.append("        u = (uint64_t)n;")
    self._output.append("    }")
    self._output.append("    ailang_write_u64(out, u);")
    self._output.append("}")
    self._output.append("")

    self._output.append("static void ailang_write_str(FILE *out, const char *s) {")
    self._output.append('    fputs(s ? s : "null", out);')
    self._output.append("}")
    self._output.append("")

    self._output.append("static void ailang_write_bool(FILE *out, int64_t v) {")
    self._output.append('    fputs(v ? "true" : "false", out);')
    self._output.append("}")
    self._output.append("")

    self._output.append("static void ailang_write_hex_u64(FILE *out, uint64_t n) {")
    self._output.append('    static const char hex_digits[] = "0123456789ABCDEF";')
    self._output.append("    fputc('0', out);")
    self._output.append("    fputc('x', out);")
    self._output.append("    int started = 0;")
    self._output.append("    for (int shift = 60; shift >= 0; shift -= 4) {")
    self._output.append("        unsigned nib = (unsigned)((n >> shift) & 0xFULL);")
    self._output.append("        if (nib || started || shift == 0) {")
    self._output.append("            fputc(hex_digits[nib], out);")
    self._output.append("            started = 1;")
    self._output.append("        }")
    self._output.append("    }")
    self._output.append("}")
    self._output.append("")

    self._output.append("static void ailang_write_bin_u64(FILE *out, uint64_t n) {")
    self._output.append("    fputc('0', out);")
    self._output.append("    fputc('b', out);")
    self._output.append("    int started = 0;")
    self._output.append("    for (int bit = 63; bit >= 0; bit--) {")
    self._output.append("        int v = (int)((n >> bit) & 1U);")
    self._output.append("        if (v || started || bit == 0) {")
    self._output.append("            fputc(v ? '1' : '0', out);")
    self._output.append("            started = 1;")
    self._output.append("        }")
    self._output.append("    }")
    self._output.append("}")
    self._output.append("")

    self._output.append("static void ailang_write_oct_u64(FILE *out, uint64_t n) {")
    self._output.append("    char tmp[24];")
    self._output.append("    int i = 0;")
    self._output.append("    do {")
    self._output.append("        tmp[i++] = (char)('0' + (n & 7U));")
    self._output.append("        n >>= 3;")
    self._output.append("    } while (n != 0U);")
    self._output.append("    fputc('0', out);")
    self._output.append("    fputc('o', out);")
    self._output.append("    while (i > 0) fputc(tmp[--i], out);")
    self._output.append("}")
    self._output.append("")
    self._output.append("#endif")
    self._output.append("")
